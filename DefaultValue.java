//Q-1.2 Write a Java program to display the default value of all primitive data types of Java

public class DefaultValue {

	static boolean value;
	static byte a;
	static char ch;
	static short sh;
	static int num;
	static float fl;
	static double dl;
	static long var;

	public static void main(String[] args) {

		System.out.println("The default value for the field boolean dataype is : " + value);
		System.out.println("The default value for the field byte dataype is : " + a);
		System.out.println("The default value for the field char dataype is : " + ch);
		System.out.println("The default value for the field short dataype is : " + sh);
		System.out.println("The default value for the field int dataype is : " + num);
		System.out.println("The default value for the field float dataype is : " + fl);
		System.out.println("The default value for the field double dataype is : " + dl);
		System.out.println("The default value for the field long dataype is : " + var);

	}
}
